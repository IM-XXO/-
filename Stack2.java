/**
 * 给定一个长度为 N的整数数列，输出每个数左边第一个比它小的数，如果不存在则输出 −1。
 */
import java.util.Scanner;
public class Stack2 {

    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] arr = new int[n];
        for(int i=0; i<n; i++) arr[i] = sc.nextInt();
    }
}
